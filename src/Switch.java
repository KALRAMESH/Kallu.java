
public class Switch {
	public static void main(String[] args) {
		int input=4;
		switch(input) {
		case 1:
			System.out.println("one");
			break;

		case 2:
		System.out.println("Two");
		break;
		case 3:
	System.out.println("Three");
	break;
	case 4:
		System.out.println("four");
		break;
		 default:
	System.out.println("other");
	break;

}
	}
}


